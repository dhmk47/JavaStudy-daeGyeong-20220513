package j06_반복;

public class For01 {
	public static void main(String[] args) {
		
		//   초기식;  조건식; 조건변화수식
		for(int i = 0; i < 5; i++) {
			System.out.println(i + 1);
		}
		// for문 순서
		
		// 1 초기식
		// 2 조건식
		// 3 실행문
		// 4 조건변화수식
		
		// 1 조건식
		// 2 실행문
		// 3 조건변화수식
		
		for(int i = 0; i < 10; i++) {
			System.out.println(10 - i);
		}
		
	}
}